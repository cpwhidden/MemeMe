    // MARK: UICollectionViewDataSource

    override func numberOfSectionsInCollectionView(collectionView: UICollectionView) -> Int {
        //#warning Incomplete method implementation -- Return the number of sections
        return 1
    }


    override func collectionView(collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
        //#warning Incomplete method implementation -- Return the number of items in the section
        return appDelegate.memes.count
    }

    override func collectionView(collectionView: UICollectionView, cellForItemAtIndexPath indexPath: NSIndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCellWithReuseIdentifier("meme", forIndexPath: indexPath) as! UICollectionViewCell
    
        let meme = appDelegate.memes[indexPath.row]
        // Configure the cell
        let imageView = cell.viewWithTag(1) as! UIImageView
        imageView.image = meme.memedImage
    
        return cell
    }
    
    override func prepareForSegue(segue: UIStoryboardSegue, sender: AnyObject?) {
        if let id = segue.identifier {
            switch id {
            case "showDetail":
                let controller = segue.destinationViewController as! MemeDetailViewController
                let row = self.collectionView?.indexPathsForSelectedItems()[0].row
                let meme = appDelegate.memes[row!]
                controller.image = meme.memedImage
                controller.index = row
            default:
                break
            }
        }
    }
    
    func collectionView(collectionView: UICollectionView,
        layout collectionViewLayout: UICollectionViewLayout,
        sizeForItemAtIndexPath indexPath: NSIndexPath) -> CGSize {
            
